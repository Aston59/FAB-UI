				<h6>3D Printers and Manufactoring: first aid measures.</h6> 
				<p class="font-sm">
				<i>Inhalation</i><br>
				<p class="font-sm">
				Heating may release vapors which may be irritating. <br>
				Milling can cause dust and particles.<br>
				In case of inhalation of products, affected person should be moved into fresh air and kept still.<br>
				Always refer to the material datasheet. Some materials shoould not be introduced in the unit, 3D printed or milled for safety reasons.<p>
				
				<br><i>Skin</i><br>
				<p class="font-sm">
				It is unlikely that first aid will be required. 
				IF ON SKIN: Wash with plenty of soap and water.
				If skin irritation occurs: Get medical advice/attention.<p>
				
				<br><i>Eyes</i><br>
				<p class="font-sm">
				It is unlikely that first aid will be required.<br>
				Dust may be irritating to the eyes: Rinse cautiously with water for several minutes. <br>Remove contact lenses, if present and easy to do. 
				<br>Continue rinsing<br>
				Use proper Eyeglasses protection. Do not look directly inside the unit.<p>
				
				<i>Most Important Symptoms/Effects</i><br>
				<p class="font-sm">
				Acute Molten material may cause thermal burns.
				Cutting tools may cause flesh cuts and skin bruises.
